package Conceptos_Basicos.ejercicios.Tema5_E4;

public class SmartDevice {

    boolean bateria;
    double precio;
    String fabricante;
    String modelo;


    public SmartDevice() {
    }

    public SmartDevice(boolean bateria, double precio, String fabricante, String modelo) {
        this.bateria = bateria;
        this.precio = precio;
        this.fabricante = fabricante;
        this.modelo = modelo;

    }



}
